# If-else statement with a joke
hungry = True

if hungry:
    print("I'm so hungry, I could eat a horse!")
else:
    print("I'm not hungry right now.")

# Nested if statements with a pun
is_it_raining = True
have_umbrella = True

if is_it_raining:
    if have_umbrella:
        print("Looks like you've got it covered!")
    else:
        print("Looks like you're in for a rain of trouble!")
else:
    print("Enjoy the sunny weather!")

# If-elif-else statement with a pop culture reference
